package Breccia.XML.translator;


/** Extended XML ‘stream constants’ for the Breccia to X-Breccia translator.
  * This extension reserves constants in the range 174,350,000 to 174,358,467,
  * user defined constants should be outside that range.
  *
  *     @see javax.xml.stream.XMLStreamConstants
  */
public interface XStreamContants {


    /** Indicates the absence of an XML document.  Occurs on attempting to translate
      * an empty source of markup.
      */
    public static final int EMPTY = 174_358_467; }


